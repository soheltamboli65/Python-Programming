list = [5, 7, 2, 1, 3, 4, 6]

print (list)

list.append(10)                          # Add an element to the list
print(list)

list.sort()                                   # Sort the list
print(list)

list.remove(10)                          # Remove an element from the list
print(list)

print(len(list))                            # Length of the list

print(list[3])                              # Access an element by index

print(list[2:6])                           # Slicing the list

reverse=list[::-1]                       #It will print the string in reverse order.
print(reverse)
