a="Kishor"
b="Thorat"
c=a+b

print(c)                                                    #It will concatenate the two strings.

print(len(c))                                             #it will count the length of the string

print(c.lower())                                        #It will print the String into Lowercase.

print(c.upper())                                       #It will print the String into Uppercase.

print(c.replace("Kishor", "Sohel"))         #Replacing Kishor with Sohel.

print(c[2:6])                                           #It will print the letter from index number 2 to 6 of c.)

print(c[4])                                             #It will print the letter on index number 4

print(c[: : -1])                                        # It will print the string in reverse order.
